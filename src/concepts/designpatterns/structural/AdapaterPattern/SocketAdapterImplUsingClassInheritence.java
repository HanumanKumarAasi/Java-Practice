package concepts.designpatterns.structural.AdapaterPattern;

public class SocketAdapterImplUsingClassInheritence extends Socket implements SocketAdapter{

	//This is the class adapter pattern which uses inheritence concept in it(extends Socket class)
	@Override
	public Volt get120Volt() {
		return getVolt();
	}

	@Override
	public Volt get12Volt() {
		Volt v= getVolt();
		return convertVolt(v,10);
	}

	@Override
	public Volt get3Volt() {
		Volt v= getVolt();
		return convertVolt(v,40);
	}
	
	private Volt convertVolt(Volt v, int i) {
		return new Volt(v.getVolts()/i);
	}

}
